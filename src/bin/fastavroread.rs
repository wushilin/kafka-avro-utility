use clap::Parser;
use kafka_avro_utility::fastavrofile::load_bytes_file;
use std::path::PathBuf;

#[derive(Parser, Debug)]
#[command(name = "fastavroread")]
#[command(
    about = "Fast reading of Avro records from a file generated by avrogen (skips deserialization)"
)]
struct Args {
    /// Input file path
    #[arg(long, required = true)]
    in_file: PathBuf,

    /// Print the raw byte length of each record
    #[arg(long, default_value_t = false)]
    print: bool,
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let args = Args::parse();

    let (schema_str, iterator) = load_bytes_file(&args.in_file)?;

    println!("Schema loaded: {}", schema_str);

    let mut count = 0;
    for entry in iterator {
        match entry {
            Ok(bytes_entry) => {
                if args.print {
                    println!(
                        "Record {}: {} bytes",
                        count + 1,
                        bytes_entry.get_data().len()
                    );
                }
                count += 1;
                if count % 10000 == 0 {
                    println!("Processed {} records...", count);
                }
            }
            Err(e) => {
                eprintln!("Error reading record {}: {}", count + 1, e);
                break;
            }
        }
    }

    println!("Total records read: {}", count);

    Ok(())
}
